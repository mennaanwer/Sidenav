function openNav() {
  document.getElementById("FullNav").style.width = "30%";
 document.getElementById("window").style.marginLeft = "30%";	
document.getElementById('btn').style.display='none';
}

function closeNav() {
  document.getElementById("FullNav").style.width = "0%";
 document.getElementById("window").style.marginLeft = "0%";
document.getElementById('btn').style.display='';
}
